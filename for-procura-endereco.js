// loop FOR - procurando endereço

console.log("Entramos na Rua das Marrecas. Procurando o endereço...");

for (var numerocasa = 1; numerocasa < 21; numerocasa = numerocasa + 2) {
console.log("Passando em frente ao número " + numerocasa + "...");
}

console.log("Você achou o endereço! Rua das Marrecas, 21");
